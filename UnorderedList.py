class Node:
    def __init__(self, initdata):
        self.data = initdata
        self.next = None

    def getData(self):
        return self.data

    def getNext(self):
        return self.next

    def setData(self,newdata):
        self.data = newdata

    def setNext(self,newnext):
        self.next = newnext


class UnorderedList:

    def __init__(self):
        self.head = None

    def isEmpty(self):
        return self.head == None

    def add(self, item):
        temp = Node(item)
        temp.setNext(self.head)
        self.head = temp

    def size(self):
        current = self.head
        count = 0
        while current != None:
            count = count + 1
            current = current.getNext()

        return count

    def search(self, item):
        current = self.head
        found = False
        while current != None and not found:
            if current.getData() == item:
                found = True
            else:
                current = current.getNext()

        return found

    def remove(self, item):
        current = self.head
        previous = None
        found = False
        while not found:  # start while loop to iterate through the list
            if current.getData() == item:
                found = True  # switches flag to True if found
            else:  # else keep iterating downward
                previous = current
                current = current.getNext()

        if previous == None:  # reached end of linked list OR linked list only has head w/ 1 Node
            self.head = current.getNext()  # get data from first Node in linked list
        else:
            previous.setNext(current.getNext())

